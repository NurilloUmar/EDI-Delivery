extension UIApplication {
    
    class func getTopViewController(base: UIViewController? = UIApplication.shared.key?.rootViewController) -> UIViewController? {
        
        if let nav = base as? UINavigationController {
            return getTopViewController(base: nav.visibleViewController)
            
        } else if let tab = base as? UITabBarController, let selected = tab.selectedViewController {
            return getTopViewController(base: selected)
            
        } else if let presented = base?.presentedViewController {
            return getTopViewController(base: presented)
        }
        return base
    }
}

extension UIApplication {
    var key: UIWindow? {
        self.connectedScenes
            .map({$0 as? UIWindowScene})
            .compactMap({$0})
            .first?
            .windows
            .filter({$0.isKeyWindow})
            .first
    }
}

extension UIImage {
    public func resized(to target: CGSize) -> UIImage {
        let ratio = min(
            target.height / size.height, target.width / size.width
        )
        let new = CGSize(
            width: size.width * ratio, height: size.height * ratio
        )
        let renderer = UIGraphicsImageRenderer(size: new)
        return renderer.image { _ in
            self.draw(in: CGRect(origin: .zero, size: new))
        }
    }
}

extension UITextView {
    func toHTML() -> String? {
        let attributedText = self.attributedText
        
        let documentAttributes: [NSAttributedString.DocumentAttributeKey: Any] = [
            .documentType: NSAttributedString.DocumentType.html,
            .characterEncoding: String.Encoding.utf8.rawValue
        ]
        
        do {
            let htmlData = try attributedText?.data(from: NSRange(location: 0, length: attributedText?.length ?? 0), documentAttributes: documentAttributes)
            if let htmlString = htmlData.flatMap({ String(data: $0, encoding: .utf8) }) {
                return htmlString
            }
        } catch {
            print("Error converting text to HTML: \(error.localizedDescription)")
        }
        return nil
    }
}
